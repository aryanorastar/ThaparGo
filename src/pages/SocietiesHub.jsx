import React, { useEffect } from 'react';

/**
 * SocietiesHub Component
 * 
 * This component redirects to the external Thapar Societies Hub website
 */
const SocietiesHub = () => {
  useEffect(() => {
    // Redirect to the external Thapar Societies Hub website
    window.location.href = 'https://thaparsocietieshub.netlify.app';
  }, []);

  // This return statement won't be visible as the redirect happens immediately
  return (
    <div className="flex items-center justify-center h-screen">
      <p>Redirecting to Thapar Societies Hub...</p>
    </div>
  );
};

export default SocietiesHub;
